"use client"

import { useState, useEffect } from "react"
import { GlassCard } from "@/components/ui/glass-card"
import { Button } from "@/components/ui/button"
import { Download, Loader2, FileText, Users } from "lucide-react"
import { getQuizResultsAction } from "@/app/actions-lms"
import jsPDF from "jspdf"
import autoTable from "jspdf-autotable"
import { toast } from "sonner"

interface QuizResultsTableProps {
    quizId: string
}

export function QuizResultsTable({ quizId }: QuizResultsTableProps) {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const [data, setData] = useState<any>(null)
    const [loading, setLoading] = useState(true)

    useEffect(() => {
        const fetchResults = async () => {
            try {
                const res = await getQuizResultsAction(quizId)
                if (res.success) {
                    setData(res.quiz)
                } else {
                    toast.error(res.error || "Failed to load results")
                }
            } catch (error) {
                toast.error("An error occurred")
            } finally {
                setLoading(false)
            }
        }

        fetchResults()
    }, [quizId])

    const generateIndividualPDF = (attempt: any) => {
        const doc = new jsPDF()

        // Header
        doc.setFontSize(20)
        doc.setTextColor(40, 40, 40)
        doc.text("EduConnect - Student Result", 20, 20)

        // Quiz Details
        doc.setFontSize(12)
        doc.text(`Quiz: ${data.title}`, 20, 35)
        doc.text(`Date: ${new Date(attempt.completedAt).toLocaleDateString()}`, 20, 42)

        // Student Details
        doc.setLineWidth(0.5)
        doc.line(20, 48, 190, 48)
        doc.text(`Student Name: ${attempt.student.name || "Unknown"}`, 20, 60)
        doc.text(`Student Email: ${attempt.student.email}`, 20, 67)

        // Score
        doc.setFontSize(16)
        doc.setTextColor(0, 100, 0)
        doc.text(`Score: ${attempt.score} / ${data.questions.length}`, 190, 60, { align: "right" })

        // Footer
        doc.setFontSize(10)
        doc.setTextColor(150, 150, 150)
        doc.text("Generated by EduConnect AI System", 105, 280, { align: "center" })

        doc.save(`${attempt.student.name}_${data.title}_Result.pdf`)
        toast.success("PDF Downloaded")
    }

    const generateClassPDF = () => {
        if (!data || !data.attempts) return

        const doc = new jsPDF()

        // Header
        doc.setFontSize(18)
        doc.text(`Class Results: ${data.title}`, 14, 20)
        doc.setFontSize(11)
        doc.text(`Course: ${data.course?.name || "N/A"}`, 14, 30)
        doc.text(`Total Attempts: ${data.attempts.length}`, 14, 36)

        // Table
        const tableData = data.attempts.map((attempt: any) => [
            attempt.student.name || "Unknown",
            attempt.student.email,
            `${attempt.score} / ${data.questions.length}`,
            new Date(attempt.completedAt).toLocaleDateString()
        ])

        autoTable(doc, {
            startY: 45,
            head: [['Student Name', 'Email', 'Score', 'Date']],
            body: tableData,
            theme: 'grid',
            headStyles: { fillColor: [66, 133, 244] }, // Google Blue :)
        })

        doc.save(`${data.title}_Class_Report.pdf`)
        toast.success("Class Report Downloaded")
    }

    if (loading) {
        return (
            <div className="flex justify-center items-center p-12">
                <Loader2 className="w-8 h-8 animate-spin text-blue-400" />
            </div>
        )
    }

    if (!data) return <div className="text-center p-8">Results not found.</div>

    return (
        <div className="space-y-6">
            <div className="flex justify-between items-center">
                <div>
                    <h2 className="text-xl font-bold">{data.title} Results</h2>
                    <p className="text-muted-foreground text-sm">
                        {data.attempts.length} student submissions found.
                    </p>
                </div>
                <Button onClick={generateClassPDF} variant="outline" className="gap-2">
                    <FileText className="w-4 h-4" /> Download Class Report
                </Button>
            </div>

            <GlassCard className="overflow-hidden">
                <div className="overflow-x-auto">
                    <table className="w-full text-sm text-left">
                        <thead className="text-xs uppercase bg-white/5 text-muted-foreground">
                            <tr>
                                <th className="px-6 py-3">Student</th>
                                <th className="px-6 py-3">Score</th>
                                <th className="px-6 py-3">Date</th>
                                <th className="px-6 py-3 text-right">Actions</th>
                            </tr>
                        </thead>
                        <tbody>
                            {data.attempts.length === 0 ? (
                                <tr>
                                    <td colSpan={4} className="px-6 py-8 text-center text-muted-foreground">
                                        No attempts yet.
                                    </td>
                                </tr>
                            ) : (
                                // eslint-disable-next-line @typescript-eslint/no-explicit-any
                                data.attempts.map((attempt: any) => (
                                    <tr key={attempt.id} className="border-b border-white/5 hover:bg-white/5 transition-colors">
                                        <td className="px-6 py-4">
                                            <div className="flex items-center gap-3">
                                                <div className="w-8 h-8 rounded-full bg-blue-500/20 flex items-center justify-center text-blue-400">
                                                    <Users className="w-4 h-4" />
                                                </div>
                                                <div>
                                                    <div className="font-medium">{attempt.student.name || "Unknown"}</div>
                                                    <div className="text-xs text-muted-foreground">{attempt.student.email}</div>
                                                </div>
                                            </div>
                                        </td>
                                        <td className="px-6 py-4 font-bold">
                                            <span className={`${(attempt.score / data.questions.length) > 0.7
                                                    ? "text-green-400"
                                                    : (attempt.score / data.questions.length) > 0.4
                                                        ? "text-yellow-400"
                                                        : "text-red-400"
                                                }`}>
                                                {attempt.score} / {data.questions.length}
                                            </span>
                                        </td>
                                        <td className="px-6 py-4 text-muted-foreground">
                                            {new Date(attempt.completedAt).toLocaleDateString()}
                                        </td>
                                        <td className="px-6 py-4 text-right">
                                            <Button
                                                size="sm"
                                                variant="ghost"
                                                onClick={() => generateIndividualPDF(attempt)}
                                                className="text-blue-400 hover:text-blue-300 hover:bg-blue-400/10"
                                            >
                                                <Download className="w-4 h-4 mr-1" /> PDF
                                            </Button>
                                        </td>
                                    </tr>
                                ))
                            )}
                        </tbody>
                    </table>
                </div>
            </GlassCard>
        </div>
    )
}
